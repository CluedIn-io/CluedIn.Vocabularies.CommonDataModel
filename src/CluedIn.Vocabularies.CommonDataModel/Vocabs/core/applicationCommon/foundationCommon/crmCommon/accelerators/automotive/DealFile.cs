    public class DealFileVocabulary : SimpleVocabulary
    {
        public DealFileVocabulary()
        {
            VocabularyName = "Deal File";
            KeyPrefix = "commonDataModel.dealfile";
            KeySeparator = ".";
            Grouping = CommonDataModelEntityTypes.DealFile;

            AddGroup("DealFile Details for Automotive", group =>
            {
                DealFileId = group.Add(new VocabularyKey(nameof(DealFileId), "Deal File", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible));
                Description = group.Add(new VocabularyKey(nameof(Description), "Description", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible));
