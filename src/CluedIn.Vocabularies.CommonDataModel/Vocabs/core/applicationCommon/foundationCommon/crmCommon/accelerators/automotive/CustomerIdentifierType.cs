using CluedIn.Core.Data;
using CluedIn.Core.Data.Vocabularies;

namespace CluedIn.Crawling.CommonDataModel.Vocabularies.Automotive
{
    public class CustomerIdentifierTypeVocabulary : SimpleVocabulary
    {
        public CustomerIdentifierTypeVocabulary()
        {
            VocabularyName = "Customer Identifier Type";
            KeyPrefix = "commonDataModel.customeridentifiertype.automotive";
            KeySeparator = ".";
            Grouping = "/CustomerIdentifierType";

            AddGroup("CustomerIdentifierType Details for Automotive", group =>
            {
			    CustomerIdentifierTypeId = group.Add(new VocabularyKey(nameof(CustomerIdentifierTypeId), "Customer Identifier Type", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible)); 
			    Description = group.Add(new VocabularyKey(nameof(Description), "Description", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible)); 
			    Name = group.Add(new VocabularyKey(nameof(Name), "Name", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible)); 
			    StateCode = group.Add(new VocabularyKey(nameof(StateCode), "Status", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible)); 
			    StatusCode = group.Add(new VocabularyKey(nameof(StatusCode), "Status Reason", VocabularyKeyDataType.Text, VocabularyKeyVisibility.Visible));             
            });
        }

        public VocabularyKey CustomerIdentifierTypeId { get; private set; }
        public VocabularyKey Description { get; private set; }
        public VocabularyKey Name { get; private set; }
        public VocabularyKey StateCode { get; private set; }
        public VocabularyKey StatusCode { get; private set; }
    }
}